fn generateArgs(allocator: mem.Allocator, interface: []const u8, args: std.ArrayList(protocol.Arg), prefix: []const u8, map: std.StringHashMap([]const u8)) ![]const u8 {
    var list: std.ArrayList(u8) = .empty;

    try list.print(allocator, "{s}: *{s}", .{ snakeCase(interface), try pascalCase(allocator, interface) });
    outer: for (args.items) |arg| {
        if (arg.type == .new_id) {
            if (arg.interface == null) {
                try list.appendSlice(allocator, ", comptime T: type, version_: u32");
            }
            continue :outer;
        }
        const arg_type = if (arg.type == .fd)
            "std.os.linux.fd_t"
        else
            try argTypeString(allocator, arg, prefix, map);
        try list.print(allocator, ", {s}: {s}", .{ arg.name, arg_type });
    }

    return list.toOwnedSlice(allocator);
}

fn generateArgsWithoutInterface(
    allocator: mem.Allocator,
    args: std.ArrayList(protocol.Arg),
    prefix: []const u8,
    map: std.StringHashMap([]const u8),
) ![]const u8 {
    var list: std.ArrayList(u8) = .empty;

    outer: for (args.items) |arg| {
        if (arg.type == .new_id) {
            if (arg.interface == null) {
                try list.appendSlice(allocator, ", comptime T: type");
            }
            continue :outer;
        }
        const arg_type = if (arg.type == .fd)
            "std.os.linux.fd_t"
        else
            try argTypeString(allocator, arg, prefix, map);
        try list.print(allocator, ", {s}: {s}", .{ arg.name, arg_type });
    }

    return mem.trimStart(u8, try list.toOwnedSlice(allocator), ",");
}

fn generateReturnType(allocator: mem.Allocator, args: std.ArrayList(protocol.Arg), current_prefix: []const u8, map: std.StringHashMap([]const u8)) ![]const u8 {
    for (args.items) |arg| {
        if (arg.type == .new_id) {
            if (arg.interface) |interface| {
                return try mem.concat(allocator, u8, &[_][]const u8{ "*", try qualifiedPascalCase(allocator, interface, current_prefix, map) });
            } else {
                return "*T";
            }
        }
    }
    return "void";
}

